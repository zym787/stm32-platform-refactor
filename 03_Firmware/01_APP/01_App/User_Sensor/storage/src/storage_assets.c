/******************************************************************************
 * @file storage_assets.c
 *
 * @par dependencies
 * - service_storage_facade.h
 * - cfg_storage.h
 * - lvgl.h
 *
 * @author Ethan-Hang
 *
 * @brief LVGL pointer-sprite assets that live on the external W25Q64 flash.
 *
 *        At first boot, the LVGL sub-region is unprovisioned (0xFF).  This
 *        translation unit:
 *
 *          1. Holds a RAM mirror buffer for each pointer image.
 *          2. Exports a parallel set of `lv_img_dsc_t` descriptors
 *             (`*_ext`) whose `.data` points at those RAM buffers.
 *          3. Implements storage_assets_bootstrap() which reads the magic,
 *             writes the original .rodata image data into the external
 *             flash on a magic mismatch, and finally re-reads the three
 *             sprites back into the RAM mirrors.
 *
 *        setup_scr_screen.c references the `*_ext` descriptors so the
 *        analog clock needles draw from the RAM mirror that is fed by the
 *        external flash.  The original `_fen_alpha_70x5_map` etc. arrays
 *        remain in .rodata only so we can self-bootstrap a freshly-erased
 *        chip without an external programming step.
 *
 * @version V1.0 2026-05-08
 *
 * @note 1 tab == 4 spaces!
 *
 *****************************************************************************/

